
<main class="container">
<div class="section">
	<h3 class="center-align">The Committee's Role in Outer Space</h3>
	<div class="divider"></div>
</div>

<div class="row">
	<div class="col s12 m10 l11">
		<ul class="timeline scrollspy" id="1950">
			<!-- First Artificial Satellite -->
			<li id="sputnik" class="timeline-inverted scrollspy">
			  <div class="timeline-badge info"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">First Artificial Satellite</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">4 October 1957</p>
			    </div>
			    <div class="timeline-body">
			    	The Soviet Union launches Sputnik I, the first successful artificial satellite, into orbit as part of the International Geophysical Year. 
			    </div>
			  </div>
			</li>

			<!-- General Assembly Resolution 1148 (XII) -->
			<li id="beginnings" class="scrollspy">
			  <div class="timeline-badge danger"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">General Assembly Resolution 1148 (XII)</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">14 November 1957</p>
			    </div>
			    <div class="timeline-body">
			    	The UN's involvement in space affairs begins with General Assembly Resolution 1148 (XII). In the context of disarmament and weapons of mass destruction, the resolution urges an agreement that includes:
			    	<blockquote>
		      		The joint study of an inspection system designed to ensure that the sending of objects through outer space shall be exclusively for peaceful and scientific purposes.
		    		</blockquote>
			    </div>
			  </div>
			</li>
			
			<!-- Ad hoc -->
			<li id="ad-hoc" class=" scrollspy">
			  <div class="timeline-badge danger"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">Ad Hoc Committee Established</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">13 December 1958</p>
			    </div>
			    <div class="timeline-body">
			    	The United Nations General Assembly establishes the Committee on the Peaceful Uses of Outer Space as an ad hoc committee in United Nations Resolution 1348 (XIII). The resolution notes in the preamble the new issues raised: 
			    	<blockquote>
		      		<i>Conscious</i> that recent developments in respect of outer space have added a new dimension to man's existence and opened new possibilities for the increase of his knowledge and the improvement of his life
		    		</blockquote>
		    		The ad hoc committee included the creation of a group of experts that would eventually become the Office for Outer Space Affairs, who now serves as the secretariat for the permanent committee. 

			    </div>
			  </div>
			</li>

			<!-- High Altitude Explosions -->
			<li id="high-altitude" class="timeline-inverted scrollspy">
			  <div class="timeline-badge info"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">High-Altitude Nuclear Tests</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">August 1958</p>
			    </div>
			    <div class="timeline-body">
			    	The United States begins high-altitude and outer space nuclear tests with Operation Argus in 1958. The goal of these tests were to observe the radiation from the detonation's use in interfering with enemy communications and weapon performance.
			    </div>
			  </div>
			</li>

			<!-- Continued Satellite Launches -->
			<li id="continued" class="timeline-inverted scrollspy">
			  <div class="timeline-badge info"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">More Satellites</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">December 1958 - February 1959</p>
			    </div>
			    <div class="timeline-body">
			    	During this time, the first weather satellites and the first communications satellites were launched. In addition, the Soviet Union's Luna I becomes the first spacecraft to reach the Moon. 
			    </div>
			  </div>
			</li>

			<!-- Permanent -->
			<li id="permanent" class=" scrollspy">
			  <div class="timeline-badge danger"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">Permanent Committee Established</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">12 December 1959</p>
			    </div>
			    <div class="timeline-body">
			    	The Committee is officially established by United Nations General Assembly Resolution 1472 (XIV) as a permament committee with the mandate to:
			    	<blockquote>
			    		To review, as appropriate, the area of international co-operation, and to study practical and feasible means for giving effect to programmes in the peaceful uses of outer space which could appropriately be under taken under United Nation auspices, including, <i>inter alia</i>
			    	</blockquote>
			    	as well as to
			    	<blockquote>
			    		To study the nature of legal problems which may arise from the exploration of outer space.
			    	</blockquote>
			    </div>
			  </div>
			</li>
		</ul>
		<ul class="timeline scrollspy" id="1960">
			<!-- First Spy Satellite -->
			<li id="spy-satellite" class="timeline-inverted scrollspy">
			  <div class="timeline-badge info"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">First Spy Satellite</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">5 July 1960</p>
			    </div>
			    <div class="timeline-body">
			    	The United States launches the first successful spy satellite under the cover name Galactic Radiation and Background (GRAB). The satellite's orbit included time over the Soviet Union and the People's Republic of China. The flyover of satellites over other nation's territory indicated that "space law" would be distinct from existing regulations.
			    </div>
			  </div>
			</li>

			<!-- Partial Nuclear Ban-->
			<li id="partial-ban" class=" scrollspy">
			  <div class="timeline-badge info"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">Partial Nuclear Test Ban Treaty</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">10 October 1963</p>
			    </div>
			    <div class="timeline-body">
			    	The Treaty Banning Nuclear Weapon Tests in the Atmosphere, in Outer Space and under Water enters in to force banning nuclear weapon tests in the atmosphere, outer space and underwater. It does not prohibit placing weapons in outer space, however.
			    </div>
			  </div>
			</li>

			<!-- Outer Space Treaty -->
			<li id="ost" class=" scrollspy">
			  <div class="timeline-badge danger"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">Outer Space Treaty</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">10 October 1967</p>
			    </div>
			    <div class="timeline-body">
			    	The first legal document in the field of law in outer space enters into force. The Outer Space Treaty became the basis from which many following agreements and conventions would follow. In particular, it limits the use of space to peaceful purposes and prevents both the presence and use of weapons of mass destruction in space.
			    </div>
			  </div>
			</li>

			<!-- UNISPACE I -->
			<li id="unispaceI" class=" scrollspy">
			  <div class="timeline-badge danger"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">UNISPACE I</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">14 to 27 August 1968</p>
			    </div>
			    <div class="timeline-body">
			    	The first UN Conference on the Exploration and Peaceful Uses of Outer Space is held in Vienna, Austria. Notable topics of discussion included communications and meteorology satellites as well as cooperation between nations, with a focus on using space technologies to benefit developing nations.
			    </div>
			  </div>
			</li>

			<!-- Rescue Agreement -->
			<li id="arra" class="scrollspy">
			  <div class="timeline-badge danger"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">Rescue Agreement</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">3 December 1968</p>
			    </div>
			    <div class="timeline-body">
			    	Expanding on the provisions regarding individuals in distress in space from the <a href="#ost"> Outer Space Treaty</a>, the agreement requires that states and nations aware of personnel in space  in need of aid must provide all available assistance regardless of circumstances.
			    </div>
			  </div>
			</li>
		</ul>
		<ul class="timeline scrollspy" id="1970">
			<!-- Liability Convention -->
			<li id="liab" class=" scrollspy">
			  <div class="timeline-badge danger"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">Liability Convention</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">September 1972</p>
			    </div>
			    <div class="timeline-body">
			    	Elaborating on Article VII of the <a href="#ost"> Outer Space Treaty</a>, the Liability Agreement requires that nations and states bear all responsibility of their spacecraft and vehicles as well as damage incurred by the vehicles.
			    </div>
			  </div>
			</li>

			<!-- Salyut 3 -->
			<li id="salyut3" class="timeline-inverted scrollspy">
			  <div class="timeline-badge info"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">Salyut 3 </h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">25 June 1974</p>
			    </div>
			    <div class="timeline-body">
			    	A military space station, Salyut 3 was allegedly armed with a gun or cannon. While never confirmed, it is alleged that the weapon was installed for "self-defense" purposes. While the <a href="#ost" >Outer Space Treaty </a> prohibits weapons of mass destruction in space, it makes no comment regarding the presence of conventional weapons in outer space. 
			    </div>
			  </div>
			</li>

			<!-- European Space Agency-->
			<li id="esa" class="timeline-inverted scrollspy">
			  <div class="timeline-badge info"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">European Space Agency</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">30 May 1975</p>
			    </div>
			    <div class="timeline-body">
			    	The Convention for the establishment of a European Space Agency creates the European Space Agency. Representing cooperation between many European nations, the ESA's purpose is:
			    	<blockquote>
			    		to provide for and to promote, for exclusively peaceful purposes, cooperation among European States in space research and technology and their space applications, with a view to their being used for scientific purposes and for operational space applications systems
			    	</blockquote>
			    </div>
			  </div>
			</li>

			<!-- Registration Convention -->
			<li id="reg" class=" scrollspy">
			  <div class="timeline-badge danger"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">Registration Convention</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">15 September 1976 </p>
			    </div>
			    <div class="timeline-body">
			    	The Registration Convention requires all launched objects to have its details registered with the Committee for the Peaceful Uses of Outer Space.
			    </div>
			  </div>
			</li>

			<!-- Kosmos 954-->
			<li id="kosmos954" class="timeline-inverted scrollspy">
			  <div class="timeline-badge info"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">Kosmos 954</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">24 January 1978</p>
			    </div>
			    <div class="timeline-body">
			    	On January 24, 1978, Kosmos 954, a Soviet surveillance satellite deorbited and crashed into Western Canada. It's power supply of around 100 lbs of radioactive uranium along with the rest of wreckage was spread over a 500-mile area. Under both the <a href="#liab"> Liability Agreement </a>and the <a href="#arra"> Rescue Agreement </a>, Canada informed the Soviet Union and the UN of the incident and submitted a claim of approximately 6 million USD for damages and cleanup. The Soviet Union paid 3 million USD after negotiations.
			    </div>
			  </div>
			</li>
		</ul>
		<ul class="timeline scrollspy" id="1980">
			<!-- UNISPACE II -->
			<li id="unispaceII" class=" scrollspy">
			  <div class="timeline-badge danger"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">UNISPACE II</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">9 to 21 August 1982</p>
			    </div>
			    <div class="timeline-body">
			    	The second UN Conference on the Exploration and Peaceful Uses of Outer Space continues its discussion of space technologies and cooperation in outer space. The concluding report notes:
			    	<blockquote>
			    		Co-operation between nations on matters concerning space has a long and rather fruitful record, though recent trends leading to the extension of the arms race into outer space are a cause of great concern to the international community. 
			    	</blockquote>
			    </div>
			  </div>
			</li>

			<!-- Moon Treaty -->
			<li id="moon" class=" scrollspy">
			  <div class="timeline-badge danger"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">Moon Treaty</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">11 July 1984</p>
			    </div>
			    <div class="timeline-body">
			    	The Moon Treaty adopted in 1979 by the General Assembly is entered into force approximately five years after with its fifth ratification. Designed to replace the <a href="#ost"> Outer Space Treaty </a> by expanding and elaborating on its provisions relating to the Moon and other celestial bodies, provisions such as those prohibiting private ownership of any or part of any celestial bodies made it unpopular. No nation capable of independent manned missions has ratified the treaty making it largely ineffective.
			    </div>
			  </div>
			</li>
		</ul>
		<ul class="timeline scrollspy" id="1990">
			<!-- International Space Station -->
			<li id="iss" class="timeline-inverted scrollspy">
			  <div class="timeline-badge danger"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">International Space Station Intergovernmental Agreement</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">28 January 1998</p>
			    </div>
			    <div class="timeline-body">
			    	The agreement sets into motion one of the largest cooperative efforts in space exploration involving fifteen governments to develop and create the International Space Station. The government explicitly declares its operation and development to be in accordance with all UN treaties on outer space with the exception of the <a href="#moon"> Moon Treaty</a>. Additionally, the treaty waives parts of the <a href="#liab">Liability Convention</a> through cross-waivers with the goal of  encouraging participation.
			    </div>
			  </div>
			</li>

			<!-- UNISPACE III -->
			<li id="unispaceIII" class=" scrollspy">
			  <div class="timeline-badge danger"></div>
			  <div class="timeline-panel">
			    <div class="timeline-heading">
			      <h5 class="timeline-title">UNISPACE III</h5>
			      <div class="divider"></div>
			      <p class="blue-grey-text text-darken-1">19 to 30 July 1999</p>
			    </div>
			    <div class="timeline-body">
			    	The third UN Conference on the Exploration and Peaceful Uses of Outer Space concluded with the adoption of the The Space Millennium: Vienna Declaration on Space and Human Development. The conference emphasized the new concerns of the Earth's environment and resources and reiterating the role of the UN in outer space affairs to facilitate cooperation. 
			    </div>
			  </div>
			</li>
		</ul>		
